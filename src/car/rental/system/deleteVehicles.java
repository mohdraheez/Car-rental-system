    public void loadData(){
        connection = Connector.ConnectDb();
        String sql = "select vid,model,type,price,lotno from vehicles where sid ='"+SID+"'";
        try {
            ps = connection.prepareStatement(sql);
            rs = ps.executeQuery();
            Object columnData[] = new Object[6];
            while (rs.next()) {
                columnData[0] = rs.getString("vid");
                columnData[1] = rs.getString("model");
                columnData[2] = rs.getString("type");
                columnData[3] = rs.getString("price");
                columnData[4] = rs.getString("lotno");
                defaultTableModel.addRow(columnData);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String uid = jTextField1.getText();
        String sql = "delete from vehicles where eid = '" + uid+"' and sid='"+SID+"'";
        try {
            ps = connection.prepareStatement(sql);
            ps.execute();
            
            JOptionPane.showMessageDialog(null, "Vehicle" + uid + " has been deleted");
            defaultTableModel.getDataVector().removeAllElements();
            defaultTableModel.fireTableDataChanged();
            loadData();
           
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Vehicle not found "+e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

